#!/bin/sh 

# Перейдите в корень репозитория (необязательно, измените по мере необходимости) 
cd /path/to/your/repo 

# Подготовьте все удаленные файлы к удалению 
git ls-files --deleted -z | xargs -0 git rm 

# Зафиксируйте изменения 
git commit -m "Удалить удаленные файлы из репозитория" 

# Отправьте в удаленный репозиторий 
git push origin main 

echo "Удаленные файлы были удалены из репозитория, а изменения отправлены в удаленный репозиторий."
